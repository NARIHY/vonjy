# Gendarmerie Post Details
INSERT INTO gendarmerie_post_details (secours_post_id, rural_or_urban, jurisdiction, created_at, updated_at)
SELECT
    id,
    CASE
        WHEN address ILIKE '%ville%' OR address ILIKE '%urban%' OR address ILIKE '%Antananarivo%' THEN 'urban'
        ELSE 'rural'
    END AS rural_or_urban,
    address AS jurisdiction,
    NOW(),
    NOW()
FROM secours_posts
WHERE type = 'gendarmerie';

